package _11_basicAPIClass._15_format;

// 숫자 형식 클래스(DecimalFormat): 숫제 다에터를 원하는 형식으로 표현하기 위해서 패턴을 사용.
// -------------------------------------------------------------------------------
// 기호       의미                          패턴 예            1234567.89 -> 변환 결과
// -------------------------------------------------------------------------------
// 0        10진수(빈자리는 0으로 채움)       0                   1234568
//                                      0.0                 1234567.9
//                                      0000000000.00000    0001234567.89000
// -------------------------------------------------------------------------------
// #        10진수(빈자리는 채우지 않음)      #                    1234568
//                                      #.#                 1234567.9
//                                      ##########.#####    1234567.89
// -------------------------------------------------------------------------------
// .        소수점                        #                   1234567.9
// -------------------------------------------------------------------------------
// -        음수 기호                     +#.0                 +1234567.9
//                                      -#.0                -1234567.9
// -------------------------------------------------------------------------------
// ,        단위 구분                     #,###.0              123,456,7.9
// -------------------------------------------------------------------------------
// E        지수 문자                     0.0E0                1.2E6
// -------------------------------------------------------------------------------
// ;        양수와 음수의 패턴을 모두 기술할    +#,### ; -#,###      +1,234,568(양수일 때)
//          경우, 패턴 구분자                                    -1,234,568(음수일 때)
// -------------------------------------------------------------------------------
// %        100을 곱한 후에 % 문자 붙임      #,# %                123456789%
// -------------------------------------------------------------------------------
// \u00A4   통화 기호                     \u00A4 #,###         `1,234,568 (원화 표시가 안되는군..)
// -------------------------------------------------------------------------------

import java.text.DecimalFormat;

public class DecimalFormatEx1 {
    public static void main(String[] args) {
        double num = 1234567.89;

        DecimalFormat df = new DecimalFormat("#,# %");
        System.out.println(df.format(num));

        df = new DecimalFormat("\u00A4 #,###");
        System.out.println(df.format(num));
    }
}
